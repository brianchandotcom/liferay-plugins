package com.liferay.marketplace.service.persistence;

import com.liferay.marketplace.model.App;
import com.liferay.portal.kernel.dao.orm.QueryUtil;
import com.liferay.portal.kernel.dao.orm.SQLQuery;
import com.liferay.portal.kernel.dao.orm.Session;
import com.liferay.portal.kernel.dao.orm.Type;
import com.liferay.portal.kernel.exception.SystemException;
import com.liferay.portal.service.persistence.impl.BasePersistenceImpl;
import com.liferay.util.dao.orm.CustomSQLUtil;

import java.util.List;

/**
 * @author Ryan Park
 */
public class AppFinderImpl
	extends BasePersistenceImpl<App> implements AppFinder {

	public static final String FIND_CATEGORIES =
		AppFinder.class.getName() + ".findCategories";

	public List<String> findCategories() throws SystemException {
		Session session = null;

		try {
			session = openSession();

			String sql = CustomSQLUtil.get(FIND_CATEGORIES);

			SQLQuery q = session.createSQLQuery(sql);

			q.addScalar("category", Type.STRING);

			return (List<String>)QueryUtil.list(
				q, getDialect(), QueryUtil.ALL_POS, QueryUtil.ALL_POS);
		}
		catch (Exception e) {
			throw new SystemException(e);
		}
		finally {
			closeSession(session);
		}
	}

}